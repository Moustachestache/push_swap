static int	count_itoa(long int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}

char	*ft_itoa(int n)
{
	int				i;
	long int		j;
	char			*itoa;

	j = n;
	i = count_itoa(j);
	itoa = ft_calloc(i-- + 1, sizeof(char));
	if (j < 0)
	{
		j *= -1;
		itoa[0] = '-';
	}
	while (j > 9)
	{
		itoa[i] = (j % 10) + '0';
		j /= 10;
		i--;
	}
	itoa[i] = j + '0';
	return (itoa);
}
